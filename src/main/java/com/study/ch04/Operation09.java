package com.study.ch04;

public class Operation09 {
    public static void main(String[] args) {
        /*
        * << 증감연산자 >>
           선증가 ++i
           후증가 i++
        * */

        int i = 0;
        // i += 1; // 1씩 증가
        // i++; //1씩 증가 (1만 가능)
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i--);
        System.out.println(i);

    }
}
