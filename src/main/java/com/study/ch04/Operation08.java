package com.study.ch04;

public class Operation08 {
    public static void main(String[] args) {
        /*
        * 복합대입연산(산술연산, 대입연산)
        *
        * */

        int num1 = 10;
        int num2 = 20;
        num1 = num1 + num2;
        num1 += num2; // 자기 자신한테 num2까지 더하여 대입
        num1 *= num2;

    }
}
